    def request(self, method, url,
                params=None,
                data=None,
                headers=None,
                cookies=None,
                files=None,
                auth=None,
                timeout=2,
                allow_redirects=True,
                proxies=None,
                hooks=None,
                stream=None,
                verify=None,
                cert=None,
                json=None):

        if headers:
            # TODO 'user-agent'可能是大写, 这样就有些问题
            headers.update({'user-agent': choice(self._user_agent_list)})
        else:
            headers = {'user-agent': choice(self._user_agent_list)}

        req = Request(
            method=method.upper(),
            url=url,
            headers=headers,
            files=files,
            data=data or {},
            json=json,
            params=params or {},
            auth=auth,
            cookies=cookies,
            hooks=hooks,
        )
        prep = self.prepare_request(req)

        proxies = proxies or {}

        settings = self.merge_environment_settings(
            prep.url, proxies, stream, verify, cert
        )

        send_kwargs = {
            'timeout': timeout,
            'allow_redirects': allow_redirects,
        }
        send_kwargs.update(settings)

        message = '%s: %s' % (method, prep.url)
        logger.info(message)
        for i in range(4):
            try:
                r = self.send(prep, **send_kwargs)
                r.raise_for_status()
                return r
            except Timeout:
                why = 'Timeout'
            except HTTPError:
                why = '%s' % r.status_code
            if i != 3:
                logger.warning('Retry %d >>> %s' % (i + 1, message))
                sleep(1)
        logger.error('[%s] %s' % (why, message))
